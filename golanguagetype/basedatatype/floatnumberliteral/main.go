package main

func main() {
	// 直接用十进制表示的浮点值
	3.1415
	.15 // 整数部分如果为0，整数部分可以省略不写
	81.80
	82. // 小数部分如果为0，小数点后的0可以省略不写

	// 科学计数法 之 十进制科学计数法形式
	6674.28e-2 // 6674.28 * 10^(-2) = 66.742800
	.12345e+5  // 0.12345 * 10^5 = 12345.000000

	// 科学计数法 之 十六进制科学计数法形式
	0x2.p10  // 2.0 * 2^10 = 2048.000000
	0x1.Fp+0 // 1.9375 * 2^0 = 1.937500

}
